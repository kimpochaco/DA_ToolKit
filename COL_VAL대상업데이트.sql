UPDATE ENC_COL_MAS
SET COLVAL_EXEC_TYP = 'N'
WHERE COLVAL_EXEC_TYP = 'Y'
AND COL_DATA_TYPE IN ('VARCHAR','NVARCHAR','CHAR','NCHAR')
AND COL_KIND > 200
AND UPPER(COL_NM) NOT LIKE '%NO'
AND UPPER(COL_NM) NOT LIKE '%ID'
AND UPPER(COL_NM) NOT LIKE '%CODE'
AND UPPER(COL_NM) NOT LIKE '%CODE1'
AND UPPER(COL_NM) NOT LIKE '%CODE2'
AND UPPER(COL_NM) NOT LIKE '%IP_ADDRESS'
AND UPPER(COL_NM) NOT LIKE '%TO_ADDRESS'
AND UPPER(COL_NM) NOT LIKE '%USER'
AND UPPER(COL_NM) NOT LIKE '%RECEIVER'
AND UPPER(COL_NM) NOT LIKE '%TYPE'

COMMIT;

