--날짜 컬럼 찾기 1
SELECT TOP_COL_PAT
     , MIN_COL_VAL, MAX_COL_VAL, A.*
FROM ENC_COL_MAS A
WHERE 1=1 
AND COLVAL_EXEC_TYP = 'Y'
AND (COL_COMMENT LIKE '%날짜%' 
		OR COL_COMMENT LIKE '%날자%'
		OR COL_COMMENT LIKE '%일자%'
		OR COL_COMMENT LIKE '%일시%'
		OR UPPER(COL_NM) LIKE '%DATE%' 
		OR UPPER(COL_NM) LIKE '%DT%' 
		OR UPPER(COL_NM) LIKE '%TIME%'
		OR UPPER(COL_NM) LIKE '%DTTM%'
		OR UPPER(COL_NM) LIKE '%DTM%'
      OR UPPER(COL_NM) LIKE '%DAY%')
AND COL_DATA_TYPE NOT IN ('DATETIME','DATETIME2', 'DATE','SMALLDATETIME')
AND UPPER(COL_NM) NOT LIKE '%YN'
AND UPPER(COL_NM) NOT LIKE '%REASON'
AND UPPER(COL_NM) NOT LIKE '%DESCR'
AND UPPER(COL_NM) NOT LIKE '%ID'
AND UPPER(COL_NM) NOT LIKE '%NAME'
AND UPPER(COL_NM) NOT LIKE '%NM'
AND UPPER(COL_NM) NOT LIKE '%NO'
AND UPPER(COL_NM) NOT LIKE '%CD'


UPDATE ENC_COL_MAS
SET COLVAL_EXEC_TYP = 'A'
WHERE 1=1 
AND (COL_COMMENT LIKE '%날짜%' OR UPPER(COL_NM) LIKE '%DATE%')
AND COL_DATA_TYPE NOT IN ('DATETIME','DATETIME2', 'DATE','SMALLDATETIME')
AND UPPER(COL_NM) NOT LIKE '%YN'
AND UPPER(COL_NM) NOT LIKE '%REASON';

COMMIT;

